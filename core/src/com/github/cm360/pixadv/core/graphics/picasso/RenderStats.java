package com.github.cm360.pixadv.core.graphics.picasso;

public class RenderStats {

	private int uniqueEntities;
	private int duplicateEntities;
	private int totalEntities;

	public int getUniqueEntities() {
		return uniqueEntities;
	}

	public void setUniqueEntities(int uniqueEntities) {
		this.uniqueEntities = uniqueEntities;
	}

	public int getDuplicateEntities() {
		return duplicateEntities;
	}

	public void setDuplicateEntities(int duplicateEntities) {
		this.duplicateEntities = duplicateEntities;
	}

	public int getTotalEntities() {
		return totalEntities;
	}

	public void setTotalEntities(int totalEntities) {
		this.totalEntities = totalEntities;
	}

}
